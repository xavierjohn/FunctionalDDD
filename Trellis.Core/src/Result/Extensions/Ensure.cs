namespace Trellis;

using System;
using System.Diagnostics;

/// <summary>
/// Provides extension methods for ensuring conditions on Result values.
/// If conditions fail, the result is converted to a failure with the specified error.
/// </summary>
/// <remarks>
/// <para>
/// Ensure is the <strong>canonical guard primitive</strong> in Trellis. Prefer it over hand-written
/// <c>if (!cond) return Result.Fail(error);</c> blocks — Ensure participates in tracing, composes
/// inside railway pipelines, and threads through both <see cref="Result"/> and <see cref="Result{T}"/>.
/// </para>
/// <para>
/// For non-generic guards (no value to thread), call <see cref="Result.Ensure(bool, Error)"/> as a
/// static factory: <c>Result.Ensure(condition, error)</c> — it returns <c>Result.Ok()</c> when
/// <c>condition</c> is <c>true</c> and <c>Result.Fail(error)</c> otherwise.
/// </para>
/// <para>
/// For value-threaded guards, call <c>result.Ensure(predicate, error)</c> as an extension on
/// <see cref="Result{T}"/>. The extension shape short-circuits on failure: if the receiver is
/// already a failure, the predicate is not evaluated and the original error is preserved.
/// </para>
/// </remarks>
/// <example>
/// <code>
/// // Non-generic guard at the top of a handler:
/// var guard = Result.Ensure(order.IsOpen, new Error.Conflict(null, "order_closed"));
///
/// // Value-threaded guard inside a pipeline:
/// var validated = parseAmount
///     .Ensure(amt =&gt; amt &gt; 0, Error.InvalidInput.ForField("amount", "must_be_positive"));
/// </code>
/// </example>
[DebuggerStepThrough]
public static class EnsureExtensions
{
    /// <summary>
    /// Returns a new failure result if the predicate is false. Otherwise returns the starting result.
    /// </summary>
    /// <typeparam name="TValue">Type of the result value.</typeparam>
    /// <param name="result">The result to validate.</param>
    /// <param name="predicate">The predicate to test.</param>
    /// <param name="error">The error to return if the predicate is false.</param>
    /// <returns>The original result if success and predicate is true; otherwise a failure with the specified error.</returns>
    public static Result<TValue> Ensure<TValue>(this Result<TValue> result, Func<bool> predicate, Error error)
    {
        ArgumentNullException.ThrowIfNull(predicate);

        using var activity = RopTrace.ActivitySource.StartActivity(nameof(Ensure));
        if (result.IsSuccess && !predicate())
            return Result.Fail<TValue>(error);

        result.LogActivityStatus();
        return result;
    }

    /// <summary>
    /// Returns a new failure result if the predicate is false. Otherwise returns the starting result.
    /// </summary>
    /// <typeparam name="TValue">Type of the result value.</typeparam>
    /// <param name="result">The result to validate.</param>
    /// <param name="predicate">The predicate function to test the value.</param>
    /// <param name="error">The error to return if the predicate is false.</param>
    /// <returns>The original result if success and predicate is true; otherwise a failure with the specified error.</returns>
    public static Result<TValue> Ensure<TValue>(this Result<TValue> result, Func<TValue, bool> predicate, Error error)
    {
        ArgumentNullException.ThrowIfNull(predicate);

        using var activity = RopTrace.ActivitySource.StartActivity(nameof(Ensure));
        if (result.TryGetValue(out var value) && !predicate(value))
            return Result.Fail<TValue>(error);

        result.LogActivityStatus();
        return result;
    }

    /// <summary>
    /// Returns a new failure result if the predicate is false. Otherwise returns the starting result.
    /// The error is generated by a function that receives the value.
    /// </summary>
    /// <typeparam name="TValue">Type of the result value.</typeparam>
    /// <param name="result">The result to validate.</param>
    /// <param name="predicate">The predicate function to test the value.</param>
    /// <param name="errorPredicate">The function that generates an error from the value if the predicate is false.</param>
    /// <returns>The original result if success and predicate is true; otherwise a failure with an error from the error function.</returns>
    public static Result<TValue> Ensure<TValue>(this Result<TValue> result, Func<TValue, bool> predicate, Func<TValue, Error> errorPredicate)
    {
        ArgumentNullException.ThrowIfNull(predicate);
        ArgumentNullException.ThrowIfNull(errorPredicate);

        using var activity = RopTrace.ActivitySource.StartActivity(nameof(Ensure));
        if (result.TryGetValue(out var value) && !predicate(value))
            return Result.Fail<TValue>(errorPredicate(value));

        result.LogActivityStatus();
        return result;
    }

    /// <summary>
    /// Returns a new failure result if the predicate result is a failure. Otherwise returns the starting result.
    /// </summary>
    /// <typeparam name="TValue">Type of the result value.</typeparam>
    /// <param name="result">The result to validate.</param>
    /// <param name="predicate">The predicate function that returns a Result.</param>
    /// <returns>The original result if both it and the predicate result are successes; otherwise a failure.</returns>
    public static Result<TValue> Ensure<TValue>(this Result<TValue> result, Func<Result<TValue>> predicate)
    {
        ArgumentNullException.ThrowIfNull(predicate);

        using var activity = RopTrace.ActivitySource.StartActivity(nameof(Ensure));
        if (result.IsFailure)
        {
            result.LogActivityStatus();
            return result;
        }

        var predicateResult = predicate();

        if (predicateResult.IsFailure)
            return predicateResult.ProjectFailure<TValue>(predicateResult.Error);

        result.LogActivityStatus();
        return result;
    }

    /// <summary>
    /// Returns a new failure result if the predicate result is a failure. Otherwise returns the starting result.
    /// The predicate receives the value.
    /// </summary>
    /// <typeparam name="TValue">Type of the result value.</typeparam>
    /// <param name="result">The result to validate.</param>
    /// <param name="predicate">The predicate function that receives the value and returns a Result.</param>
    /// <returns>The original result if both it and the predicate result are successes; otherwise a failure.</returns>
    public static Result<TValue> Ensure<TValue>(this Result<TValue> result, Func<TValue, Result<TValue>> predicate)
    {
        ArgumentNullException.ThrowIfNull(predicate);

        using var activity = RopTrace.ActivitySource.StartActivity(nameof(Ensure));
        if (!result.TryGetValue(out var value))
        {
            result.LogActivityStatus();
            return result;
        }

        var predicateResult = predicate(value);

        if (predicateResult.IsFailure)
            return predicateResult.ProjectFailure<TValue>(predicateResult.Error);

        result.LogActivityStatus();
        return result;
    }

    /// <summary>
    /// Ensures a string is not null or whitespace.
    /// </summary>
    /// <param name="str">The string to validate.</param>
    /// <param name="error">The error to return if the string is null or whitespace.</param>
    /// <returns>A success result with the string if valid; otherwise a failure with the specified error.</returns>
    public static Result<string> EnsureNotNullOrWhiteSpace(this string? str, Error error)
    {
        using var activity = RopTrace.ActivitySource.StartActivity(nameof(EnsureNotNullOrWhiteSpace));
        return string.IsNullOrWhiteSpace(str) ? Result.Fail<string>(error) : Result.Ok(str);
    }

    /// <summary>
    /// Ensures the value inside the result is not null, narrowing from <c>Result&lt;T?&gt;</c> to <c>Result&lt;T&gt;</c>.
    /// Returns a failure if the value is null.
    /// </summary>
    /// <typeparam name="T">The reference type of the value.</typeparam>
    /// <param name="result">The result containing a potentially null value.</param>
    /// <param name="error">The error to return if the value is null.</param>
    /// <returns>A success result with the non-null value if present; otherwise a failure with the specified error.</returns>
    public static Result<T> EnsureNotNull<T>(this Result<T?> result, Error error) where T : class
    {
        using var activity = RopTrace.ActivitySource.StartActivity();

        if (!result.TryGetValue(out var value, out var resultError))
        {
            result.LogActivityStatus();
            return result.ProjectFailure<T>(resultError);
        }

        if (value is null)
        {
            var failure = Result.Fail<T>(error);
            failure.LogActivityStatus();
            return failure;
        }

        var success = Result.Ok(value);
        success.LogActivityStatus();
        return success;
    }

    /// <summary>
    /// Ensures the value inside the result is not null for nullable value types,
    /// narrowing from <c>Result&lt;T?&gt;</c> to <c>Result&lt;T&gt;</c>.
    /// Returns a failure if the value is null.
    /// </summary>
    /// <typeparam name="T">The value type of the value.</typeparam>
    /// <param name="result">The result containing a potentially null value type.</param>
    /// <param name="error">The error to return if the value is null.</param>
    /// <returns>A success result with the unwrapped value if present; otherwise a failure with the specified error.</returns>
    public static Result<T> EnsureNotNull<T>(this Result<T?> result, Error error) where T : struct
    {
        using var activity = RopTrace.ActivitySource.StartActivity();

        if (!result.TryGetValue(out var value, out var resultError))
        {
            var failure = result.ProjectFailure<T>(resultError);
            failure.LogActivityStatus();
            return failure;
        }

        if (!value.HasValue)
        {
            var failure = Result.Fail<T>(error);
            failure.LogActivityStatus();
            return failure;
        }

        var success = Result.Ok(value.Value);
        success.LogActivityStatus();
        return success;
    }

}